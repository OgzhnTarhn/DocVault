const bcrypt = require('bcryptjs');
const jwt    = require('jsonwebtoken');
const User   = require('../models/User');

exports.register = async (req, res) => {
    console.log('ðŸ”´ REGISTER BODY:', req.body);
    const { username, password } = req.body;
    try {
        const existing = await User.findOne({ username });
        if (existing) return res.status(400).json({ msg: 'KullanÄ±cÄ± zaten var' });

        const salt = await bcrypt.genSalt(10);
        const hash = await bcrypt.hash(password, salt);
        const user = new User({ username, password: hash });
        await user.save();
        res.status(201).json({ msg: 'KayÄ±t baÅŸarÄ±lÄ±' });
    } catch (err) {
        console.error('Register error:', err);
        res.status(500).json({ msg: 'Sunucu hatasÄ±' });
    }
};

exports.login = async (req, res) => {
    console.log('ðŸ”´ LOGIN BODY:', req.body);
    const { username, password } = req.body;
    try {
        const user = await User.findOne({ username });
        if (!user) return res.status(400).json({ msg: 'GeÃ§ersiz kimlik' });

        const isMatch = await bcrypt.compare(password, user.password);
        if (!isMatch) return res.status(400).json({ msg: 'GeÃ§ersiz kimlik' });

        const token = jwt.sign({ userId: user.id }, process.env.JWT_SECRET, { expiresIn: '1h' });
        res.json({ token });
    } catch (err) {
        console.error('Login error:', err);
        res.status(500).json({ msg: 'Sunucu hatasÄ±' });
    }
};
